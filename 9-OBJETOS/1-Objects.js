// Objetos en JavaScript: Un objeto tiene propiedades y metodos, a diferencia de un primitivo
// -- Declaracion y definicion de un Object
let persona = {
    nombre: 'Juan',
    apellido: 'Perez',
    email: 'jperez@mail.com',
    edad: 28
}
// -- Impresion del Object o atributos de un Object
console.log(persona.apellido); // Resultado: Perez
console.log(persona.edad); // Resultado: 28
console.log(persona); // Resultado: imprime todo el object