// Operadores de Asignacion (=)
let miNumero = 10;
console.log(miNumero);

// Operador de Asignacion Compuesto
// +=
miNumero +=5; // Se incrementa en 5 el valor de la variable
console.log(miNumero); // Resultado : 15

// -=
miNumero -= 3;
console.log(miNumero); // Resultado : 12

// *=
miNumero *= 4;
console.log(miNumero); // Resultado : 48

// /=
miNumero /= 3;
console.log(miNumero); // Resultado :16

// %=
miNumero %= 6;
console.log(miNumero); // Resultado : 4

// **=
miNumero **= 2;
console.log(miNumero); // Resultado : 16