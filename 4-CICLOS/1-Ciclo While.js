// Ciclo While

// Imprimir valores de 0 a 5
let contador = 0;
let repeticiones = 5;
while(contador <= repeticiones) {
    console.log(contador++); // Para incrementar de 1 en 1 en cada iteracion
}


// Imprimir valores del 9 al 3
let init = 9;
let min = 3;
while(init >= min){
    console.log(init--); // Para reducir de 1 en 1 en cada iteracion
}

