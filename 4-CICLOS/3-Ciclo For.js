
/*
Ciclo For - Especificamente cuantas veces se repite algo:

for (declaracion variable ; condicion ; incrementos/decrementos){
    bloque a ejecutar;
}
*/

// Imprimir valores de 0 a 5 con For
let i = 0;
let max = 5;

for (i = 0; i <= max ; i++) {
    console.log(i);
}

// Imprimir valores de 9 a 3 con For
let init = 9;
let min = 3;

for (i = init ; i >= min ; i--) {
    console.log(i);
}

