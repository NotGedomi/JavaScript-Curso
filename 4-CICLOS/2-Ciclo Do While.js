// Ciclo Do While - Ejecuta al menos 1 vez el codigo (Ejecuta y luego evalua la condicion)

// Imprimir valores del 0 a 5
let contador = 0;
let repeticiones = 5;

do {
    console.log(contador++); // Primero ejecutamos el contador
} while(contador <= repeticiones); // Mientras se cumpla esta condicion