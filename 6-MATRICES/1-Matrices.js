// Matrices: Arreglos de 2 dimensiones
let miMatriz = [[],[]]; // Definimos la matriz con 2 renglones ([])

// Modificamos la matriz
// Renglon 0
miMatriz[0][0] = 100; // Renglon 0, Columna 0
miMatriz[0][1] = 200; // Renglon 0, Columna 1
miMatriz[0][2] = 300; // Renglon 0, Columna 2

// Renglon 1
miMatriz[1][0] = 400; // Renglon 0, Columna 0
miMatriz[1][1] = 500; // Renglon 0, Columna 1
miMatriz[1][2] = 600; // Renglon 0, Columna 2

// Impresion de valores
console.log(`Elemento[0][1]: ${miMatriz[0][1]}`); // Resultado: Elemento[0][1]: 200