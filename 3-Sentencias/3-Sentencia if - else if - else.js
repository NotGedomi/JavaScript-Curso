// Sentencia if - else if - else
let miNumero = 0;

// Revisar si el numero es positivo, negativo o igual a 0
if (miNumero > 0){
    console.log(`El numero es positivo: ${miNumero}`);
}
else if (miNumero < 0){
    console.log(`El numero es negativo: ${miNumero}`);
}
else{
    console.log(`El numero es cero o neutro: ${miNumero}`);
}