// Sentencia if else
let miNumero = -90;

// Revisar si el numero es positivo
if(miNumero > 0){
    console.log(`El valor es positivo: ${miNumero}`);
}
else {
    console.log(`El valor ${miNumero} no es positivo`);
}

// NOTA: Si el bloque if o else solo tiene una linea de codigo, no es necesario llaves "{}"
