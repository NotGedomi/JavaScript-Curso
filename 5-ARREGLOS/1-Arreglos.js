// Arreglos : Almacenar multiples valores en una variable
let miArreglo = []; // Aca estamos definiendo el arreglo

// Nota: Los valores no inicializados, por default son "undefined"
// Nota 2: Los valores asignados son dinamicos, por lo tanto los valores pueden ser de distinto tipo
miArreglo[0] = 1; // Numerico
miArreglo[1] = 'a'; // String

//Imprimimos algun indice
console.log(miArreglo[0]); // Resultado: 1

